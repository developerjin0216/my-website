/** 오늘 날짜에 해당하는 명언 반환 */
export function getQuoteForToday(): Quote {
  const now = new Date();
  const month = now.getMonth(); // 0-11
  const day = now.getDate(); // 1-31

  const monthQuotes = quotes[month];
  const index = (day - 1) % monthQuotes.length;
  return monthQuotes[index];
}
